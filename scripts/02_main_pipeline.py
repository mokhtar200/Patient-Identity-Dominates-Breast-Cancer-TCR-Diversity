import statsmodels.formula.api as smf

def fit_icc(df, response="Shannon", group="patient_id", fixed="subset"):
    d = df[[response, group, fixed]].dropna().copy()
    d[fixed] = d[fixed].astype("category")
    md = smf.mixedlm(f"{response} ~ {fixed}", d, groups=d[group])
    mf = md.fit(reml=True, method="lbfgs")
    var_pat = float(mf.cov_re.iloc[0, 0]); var_res = float(mf.scale)
    icc = var_pat / (var_pat + var_res)
    return mf, icc, var_pat, var_res

def bootstrap_icc_ci(df, response="Shannon", group="patient_id", fixed="subset",
                     n_boot=1000, seed=20240517):
    rng = np.random.default_rng(seed)
    pats = df[group].dropna().unique(); iccs = []
    for _ in range(n_boot):
        samp = rng.choice(pats, size=len(pats), replace=True)
        parts = []
        for i, pid in enumerate(samp):
            sub = df[df[group] == pid].copy(); sub[group] = f"{pid}_{i}"
            parts.append(sub)
        bdf = pd.concat(parts, ignore_index=True)
        try:
            _, icc, _, _ = fit_icc(bdf, response, group, fixed)
            if np.isfinite(icc): iccs.append(icc)
        except Exception:
            continue
    if not iccs: return (np.nan, np.nan)
    return tuple(np.quantile(iccs, [0.025, 0.975]))

