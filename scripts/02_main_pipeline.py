import powerlaw

def fit_powerlaw(counts, xmin=None, gof_boot=0):
    counts = np.asarray(counts, float); counts = counts[counts > 0]
    with warnings.catch_warnings():
        warnings.simplefilter("ignore")
        fit = powerlaw.Fit(counts, discrete=True, xmin=xmin, verbose=False) \
              if xmin is not None else \
              powerlaw.Fit(counts, discrete=True, verbose=False)
    alpha = fit.power_law.alpha; xm = fit.power_law.xmin
    n_tail = int(np.sum(counts >= xm)); D = fit.power_law.D
    gof_p = np.nan
    if gof_boot and gof_boot > 0:
        gof_p = bootstrap_gof(counts, fit, n_boot=gof_boot)
    return dict(fit=fit, alpha=alpha, xmin=xm, n_tail=n_tail,
                tail_prop=n_tail/len(counts), D=D, gof_p=gof_p)

def bootstrap_gof(counts, fit, n_boot=200):
    counts = np.asarray(counts, float); counts = counts[counts > 0]
    xm = fit.power_law.xmin; alpha = fit.power_law.alpha; Demp = fit.power_law.D
    ntail = np.sum(counts >= xm); ntot = len(counts); ptail = ntail/ntot
    below = counts[counts < xm]
    ge = 0
    for _ in range(n_boot):
        n_t = RNG.binomial(ntot, ptail)
        synth_tail = fit.power_law.generate_random(n_t).astype(float) if n_t > 0 else np.array([])
        n_b = ntot - n_t
        synth_below = RNG.choice(below, size=n_b, replace=True) if (n_b > 0 and len(below) > 0) else np.array([])
        synth = np.concatenate([synth_tail, synth_below])
        if len(synth) < 10: continue
        try:
            with warnings.catch_warnings():
                warnings.simplefilter("ignore")
                f2 = powerlaw.Fit(synth, discrete=True, verbose=False)
            if f2.power_law.D >= Demp: ge += 1
        except Exception:
            continue
    return ge / n_boot

def vuong_compare(counts):
    counts = np.asarray(counts, float); counts = counts[counts > 0]
    with warnings.catch_warnings():
        warnings.simplefilter("ignore")
        fit = powerlaw.Fit(counts, discrete=True, verbose=False)
    out = {}
    for alt in ["lognormal", "exponential", "truncated_power_law"]:
        R, p = fit.distribution_compare("power_law", alt, normalized_ratio=True)
        out[alt] = (R, p)
    return out

