    log("=== strengthening analyses ===")

    # S1 metric benchmark (patient-level, Treg vs CD4_Memory)
    def patient_subset_metrics():
        rows = []
        for sub in SUBSET_LEVELS:
            for pid in meta_qc.loc[meta_qc.subset==sub, "patient_id"].unique():
                ids = meta_qc[(meta_qc.subset==sub)&(meta_qc.patient_id==pid)]["sample_id"]
                cc = pd.concat([samples[i] for i in ids if samples.get(i) is not None], ignore_index=True)
                if len(cc)==0: continue
                cc = cc.groupby(["cdr3aa","v","j"], as_index=False)["count"].sum()
                cc["freq"] = cc["count"]/cc["count"].sum()
                m = alpha_metrics(cc); m.update(patient_id=pid, subset=sub); rows.append(m)
        return pd.DataFrame(rows)
    psm = patient_subset_metrics()
    if len(pa): psm = psm.merge(pa, on=["patient_id","subset"], how="left")
    bench = []
    for m in ["Shannon","invSimpson","Gini","Pielou","Clonality","Top10","Hill1","Chao1","alpha"]:
        if m not in psm.columns: continue
