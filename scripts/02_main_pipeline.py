    # ---- 5. power-law (pooled, descriptive) ----
    pl_rows = []
    for s in SUBSET_LEVELS:
        f = fit_powerlaw(pools[s]["count"].values, gof_boot=GOF_BOOT)
        pl_rows.append(dict(subset=s, alpha=round(f["alpha"],3), xmin=f["xmin"],
            n_tail=f["n_tail"], tail_pct=round(100*f["tail_prop"],2),
            KS_D=round(f["D"],4), gof_p=round(f["gof_p"],3) if np.isfinite(f["gof_p"]) else np.nan))
    pd.DataFrame(pl_rows).to_csv(os.path.join(OUT_DIR, "Table3_powerlaw_pooled.csv"), index=False)
    log("Pooled power-law done: " + ", ".join(f"{r['subset']} a={r['alpha']}" for r in pl_rows))

    # Vuong-style model comparison
    vrows = []
    for s in SUBSET_LEVELS:
        cmp = vuong_compare(pools[s]["count"].values)
        vrows.append(dict(subset=s,
            vs_lognormal_R=round(cmp["lognormal"][0],3),  vs_lognormal_p=round(cmp["lognormal"][1],3),
            vs_exponential_R=round(cmp["exponential"][0],3), vs_exponential_p=round(cmp["exponential"][1],3),
            vs_truncPL_R=round(cmp["truncated_power_law"][0],3), vs_truncPL_p=round(cmp["truncated_power_law"][1],3)))
    pd.DataFrame(vrows).to_csv(os.path.join(OUT_DIR, "Table_model_comparison.csv"), index=False)

    # patient-level alpha (PRIMARY tested comparison)
    def patient_alpha(xmin=PL_XMIN_FIXED):
        res = []
        for sub in SUBSET_LEVELS:
            for pid in meta_qc.loc[meta_qc["subset"]==sub, "patient_id"].unique():
                ids = meta_qc[(meta_qc["subset"]==sub)&(meta_qc["patient_id"]==pid)]["sample_id"]
                cc = pd.concat([samples[i] for i in ids if samples.get(i) is not None],
                               ignore_index=True)
                if len(cc)==0: continue
                cc = cc.groupby(["cdr3aa","v","j"], as_index=False)["count"].sum()["count"].values
                if np.sum(cc >= xmin) < PL_MIN_TAIL: continue
                try:
                    f = fit_powerlaw(cc, xmin=xmin, gof_boot=0)
                    if np.isfinite(f["alpha"]):
                        res.append(dict(patient_id=pid, subset=sub, alpha=f["alpha"]))
                except Exception: continue
        return pd.DataFrame(res)
    pa = patient_alpha()
    pa.to_csv(os.path.join(OUT_DIR, "Table_patient_level_alpha.csv"), index=False)
    if len(pa):
        med = pa.groupby("subset")["alpha"].median()
        tr = pa[pa.subset=="Treg"]["alpha"].values; me = pa[pa.subset=="CD4_Memory"]["alpha"].values
        ot = pa[pa.subset=="Other"]["alpha"].values
        def safe_mw(a,b): return stats.mannwhitneyu(a,b)[1] if len(a)>2 and len(b)>2 else np.nan
        log(f"Patient-level alpha medians: " + ", ".join(f"{k}={v:.2f}" for k,v in med.items()))
        log(f"  Treg vs CD4_Memory p={safe_mw(tr,me):.3f} r={rank_biserial(tr,me):.2f}; "
            f"Treg vs Other p={safe_mw(tr,ot):.3f} r={rank_biserial(tr,ot):.2f}")
        pr = pd.merge(pa[pa.subset=="Treg"][["patient_id","alpha"]].rename(columns={"alpha":"treg"}),
                      pa[pa.subset=="CD4_Memory"][["patient_id","alpha"]].rename(columns={"alpha":"mem"}),
                      on="patient_id")
