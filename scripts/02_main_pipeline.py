def bray_curtis_matrix(M):
    return squareform(pdist(M, metric="braycurtis"))

def pcoa(D, k=2):
    n = D.shape[0]; D2 = D**2
    J = np.eye(n) - np.ones((n, n))/n
    B = -0.5 * J @ D2 @ J
    w, V = np.linalg.eigh(B)
    idx = np.argsort(w)[::-1]
    w, V = w[idx], V[:, idx]
    pos = w > 0
    coords = V[:, :k] * np.sqrt(np.abs(w[:k]))
    expl = w[:k] / w[pos].sum()
    return coords, expl

def permanova(D, labels, n_perm=999, seed=0):
    rng = np.random.default_rng(seed)
    labels = np.asarray(labels); n = len(labels); groups = np.unique(labels)
    D2 = D**2; SST = D2[np.triu_indices(n, 1)].sum() / n
    def ss_within(lab):
        s = 0.0
        for g in groups:
            idx = np.where(lab == g)[0]; ng = len(idx)
            if ng > 1:
                s += D2[np.ix_(idx, idx)][np.triu_indices(ng, 1)].sum() / ng
        return s
    SSW = ss_within(labels); SSA = SST - SSW
    a = len(groups)
    F = (SSA/(a-1)) / (SSW/(n-a))
    perm = np.empty(n_perm)
    for i in range(n_perm):
        lp = rng.permutation(labels)
        ssw = ss_within(lp); ssa = SST - ssw
        perm[i] = (ssa/(a-1)) / (ssw/(n-a))
    p = (np.sum(perm >= F) + 1) / (n_perm + 1)
    R2 = SSA / SST
    return dict(F=F, R2=R2, p=p, df1=a-1, df2=n-a)

def morisita_horn(x, y):
    keys = set(x) | set(y)
    xv = np.array([x.get(k, 0) for k in keys], float)
    yv = np.array([y.get(k, 0) for k in keys], float)
    px = xv/xv.sum(); py = yv/yv.sum()
    return 2*np.sum(px*py) / (np.sum(px**2) + np.sum(py**2))

