def gini(x):
    x = np.sort(np.asarray(x, float)); n = len(x)
    if n == 0 or x.sum() == 0: return np.nan
    cum = np.cumsum(x)
    return (n + 1 - 2 * np.sum(cum) / cum[-1]) / n

def alpha_metrics(df):
    p = df["freq"].values; cnt = df["count"].values
    S = len(df); H = -np.sum(p * np.log(p))
    f1 = np.sum(cnt == 1); f2 = np.sum(cnt == 2)
    chao1 = S + (f1**2 / (2*f2) if f2 > 0 else f1*(f1-1)/2)
    o = np.sort(p)[::-1]
    pielou = H/np.log(S) if S > 1 else np.nan
    return dict(Shannon=H, invSimpson=1/np.sum(p**2), Gini=gini(cnt),
                Pielou=pielou, Clonality=(1-pielou) if S > 1 else np.nan,
                Hill0=S, Hill1=np.exp(H), Hill2=1/np.sum(p**2),
                Chao1=chao1, Sobs=S, Top10=float(np.sum(o[:max(1, int(0.10*S))])))

