def _match_col(columns, aliases):
    low = {c.lower().strip(): c for c in columns}
    for a in aliases:
        if a in low:
            return low[a]
    for a in aliases:
        for lc, orig in low.items():
            if a in lc:
                return orig
    return None

def read_clonotype_table(path):
    """Read one clonotype file (CSV/TSV/txt). Returns DataFrame[cdr3aa,v,j,count]."""
    sep = "\t" if path.lower().endswith((".tsv", ".txt")) else None
    try:
        df = pd.read_csv(path, sep=sep, engine="python", compression="infer")
    except Exception as e:
        log(f"  ! could not read {os.path.basename(path)}: {e}")
        return None
    if df.shape[1] == 1:
        try: df = pd.read_csv(path, sep="\t", engine="python")
        except Exception: pass
    cols = list(df.columns)

    # filter for TRB chain if column exists
    chain_col = _match_col(cols, ["chain"])
    if chain_col is not None:
        df = df[df[chain_col].astype(str).str.upper().str.contains("TRB")]

    c_cdr3 = _match_col(cols, COLUMN_ALIASES["cdr3aa"])
    c_v    = _match_col(cols, COLUMN_ALIASES["v"])
    c_j    = _match_col(cols, COLUMN_ALIASES["j"])
    c_cnt  = _match_col(cols, COLUMN_ALIASES["count"])
    c_frq  = _match_col(cols, COLUMN_ALIASES["freq"])
    if c_cdr3 is None or c_v is None or c_j is None:
        raise ValueError(f"Missing CDR3/V/J columns in {os.path.basename(path)}. "
                         f"Columns present: {cols}")
    if c_cnt is None and c_frq is None:
        raise ValueError(f"No count or frequency column in {os.path.basename(path)}. "
                         f"Columns present: {cols}")
    out = pd.DataFrame({
        "cdr3aa": df[c_cdr3].astype(str),
        "v": df[c_v].astype(str),
        "j": df[c_j].astype(str),
    })
    if c_cnt is not None:
        out["count"] = pd.to_numeric(df[c_cnt], errors="coerce")
    else:
        fr = pd.to_numeric(df[c_frq], errors="coerce").fillna(0)
        out["count"] = np.maximum(1, np.round(fr / fr[fr > 0].min())).astype(float)
    return out

def standardise(df):
    """Apply QC/standardisation (Methods 2.2). Returns cleaned DataFrame or None."""
    if df is None or len(df) == 0:
        return None
    df = df.copy()
    df["v"] = df["v"].str.replace(r"\*.*$", "", regex=True)
    df["j"] = df["j"].str.replace(r"\*.*$", "", regex=True)
    df = df[df["cdr3aa"].notna() & (df["cdr3aa"] != "")]
    df = df[~df["cdr3aa"].str.contains(r"[*._]", regex=True, na=True)]
    df = df[df["cdr3aa"].apply(lambda s: len(s) >= 5 and len(s) <= 25 and set(s) <= AA20)]
    df = df[df["v"].str.startswith("TRBV") & df["j"].str.startswith("TRBJ")]
    df = df[pd.to_numeric(df["count"], errors="coerce").fillna(0) > 0]
    if len(df) == 0:
        return None
    df = df.groupby(["cdr3aa", "v", "j"], as_index=False)["count"].sum()
    df["freq"] = df["count"] / df["count"].sum()
    return df

